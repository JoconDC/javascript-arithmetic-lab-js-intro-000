// Basic Math Operator
function add() {
  return 2 + 2;
}
function subtract() {
  return 5 - 3;
}
function multiply() {
  return 4 * 4;
}
function divide() {
  return 12 / 3;
}
// Increment & Decrement
var n = 5
function increment(n) {
   return n ++;
}
function decrement(n) {
  return n --;
}
increment();
decrement();
